                            <h5>The ABSA</h5>
                            <p>The American Board of Surgical Assistants (ABSA), was founded in 1987, by Paul F. Weeks, MD, PhD as a national credentialing organization, for surgical assistants. The ABSA administers a national certification examination, for surgical assistants, covering all surgical disciplines and all areas of perioperative medicine. The examination evaluates candidate knowledge of surgical anatomy, procedures and techniques, diagnostic studies, emergency situations, OSHA regulations and general patient safety.</p>
                            <p>We acknowledge that the role and function of a surgical assistant is to assist the surgeon, in the performance of a surgical procedure. It is understood and acknowledged that certification as a surgical assistant through the ABSA (SA-C) does not allow for any independent performance of any medical or surgical procedures, within the United States of America or its territories.</p>
